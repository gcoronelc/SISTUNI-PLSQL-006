CREATE OR REPLACE FUNCTION SCOTT.FN_FACTORIAL
( P_NUM NUMBER)
	RETURN NUMBER
IS
	V_NUM NUMBER := P_NUM;
	V_FACT NUMBER := 1;
BEGIN
	FOR N IN 1..V_NUM LOOP
		V_FACT := V_FACT*N;	
	END LOOP;
	RETURN V_FACT;
END;

