

CREATE OR REPLACE TRIGGER SCOTT.TR_TEST_EMP
AFTER INSERT OR DELETE OR UPDATE ON SCOTT.EMP
BEGIN
  IF INSERTING THEN
  DBMS_OUTPUT.PUT_LINE('NUEVO EMPLEADO SE HA REGISTRADO');
  ELSIF UPDATING THEN
  DBMS_OUTPUT.PUT_LINE('UN EMPLEADO SE HA MODIFICADO');
  ELSIF DELETING THEN
  DBMS_OUTPUT.PUT_LINE('UN EMPLEADO SE HA ELIMINADO');
  END IF;
END;
/


INSERT INTO SCOTT.EMP(EMPNO, ENAME, SAL)
VALUES( 9797, 'GUSTAVO', 20000);

COMMIT;

UPDATE SCOTT.EMP
SET SAL = 30000
WHERE EMPNO = 9797;

COMMIT;

DELETE FROM scott.emp
WHERE EMPNO = 9797;

COMMIT;

ALTER TRIGGER "SCOTT"."TR_TEST_EMP" DISABLE;


SELECT * FROM ALL_TRIGGERS
WHERE OWNER = 'SCOTT';

SELECT TEXT FROM ALL_SOURCE 
WHERE NAME = 'TR_TEST_EMP';

