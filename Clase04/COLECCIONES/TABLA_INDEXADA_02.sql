DECLARE

	TYPE T_TYPE IS TABLE OF VARCHAR2(100) 
		INDEX BY BINARY_INTEGER;
		
	NOMBRES T_TYPE;
	
BEGIN

	-- DATOS
	NOMBRES(1) := 'GUSTAVO';
	NOMBRES(2) := 'LAURA';
	NOMBRES(3) := 'JULIO';
	NOMBRES(4) := 'LUCERO';

	-- MOSTRAR TABLA
	FOR I IN 1..NOMBRES.COUNT LOOP
		DBMS_OUTPUT.PUT_LINE(NOMBRES(I));
	END LOOP;

END;


